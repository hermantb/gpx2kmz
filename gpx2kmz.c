static char *
patch_line (char *line, char *pos)
{
  char *tp = pos;

  while (tp != line && tp[-1] == ' ') {
    tp--;
  }
  *tp = '\0';
  pos++;
  while (*pos == ' ') {
    pos++;
  }
  tp = pos;
  while (*tp) {
    tp++;
  }
  while (tp != pos && (tp[-1] == '\r' || tp[-1] == '\n')) {
    tp--;
  }
  *tp = '\0';
  return pos;
}

